package modelo;

import java.time.LocalDate;

// Representa cualquier tipo de material que pueda ser gestionado por la biblioteca
// Libros, Revistas o Audiovisuales (AV)

public class Material {
    // clave primaria en la BD
    private int id;
    // Atributos para todos los tipos de material
    private String titulo;
    private String tipoMaterial; // Libro, Revista o AV
    private String autor;
    private String categoria;
    private String estado;

    // Campos comunes
    private String ISBN;
    private String edicion;
    private LocalDate fechaPublicacion;

    // Solo para Revista
    private String issn;
    private Integer numeroPaginas;

    // Solo para AV
    private Integer duracionMinutos;
    private Integer anio;

    // Getters y Setters generados automaticamente con click derecho e insert code en el IDE de netbeans
    // permiten acceder y modificar cada propiedad del material
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getTipoMaterial() {
        return tipoMaterial;
    }

    public void setTipoMaterial(String tipoMaterial) {
        this.tipoMaterial = tipoMaterial;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getISBN() {
        return ISBN;
    }

    public void setISBN(String ISBN) {
        this.ISBN = ISBN;
    }

    public String getEdicion() {
        return edicion;
    }

    public void setEdicion(String edicion) {
        this.edicion = edicion;
    }

    public LocalDate getFechaPublicacion() {
        return fechaPublicacion;
    }

    public void setFechaPublicacion(LocalDate fechaPublicacion) {
        this.fechaPublicacion = fechaPublicacion;
    }

    public String getIssn() {
        return issn;
    }

    public void setIssn(String issn) {
        this.issn = issn;
    }

    public Integer getNumeroPaginas() {
        return numeroPaginas;
    }

    public void setNumeroPaginas(Integer numeroPaginas) {
        this.numeroPaginas = numeroPaginas;
    }

    public Integer getDuracionMinutos() {
        return duracionMinutos;
    }

    public void setDuracionMinutos(Integer duracionMinutos) {
        this.duracionMinutos = duracionMinutos;
    }

    public Integer getAnio() {
        return anio;
    }

    public void setAnio(Integer anio) {
        this.anio = anio;
    }
}

    